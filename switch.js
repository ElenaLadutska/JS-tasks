let num = +prompt('Enter a number from 1 to 4');

switch (num) {
  case 1:
    alert('winter');
    break;
  case 2:
    alert('spring');
    break;
  case 3:
    alert('summer');
    break;
  case 4:
    alert('autumn');
    break;
  default:
    alert('Invalid value. Enter a number from 1 to 4');
    break;
}
